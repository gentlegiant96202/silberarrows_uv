def get_trim_url(model: str, trim_id: int) -> str:
    """Generate full Dubizzle URL for a specific trim."""
    slug = MODEL_SLUGS.get(model, model.lower().replace(" ", "-"))
    return f"https://dubai.dubizzle.com/motors/used-cars/mercedes-benz/{slug}/?trim={trim_id}"

def get_all_trim_urls() -> list:
    """Get all trim URLs as a flat list of (model, trim_name, url) tuples."""
    urls = []
    for model, trims in TRIM_URLS.items():
        for trim_name, trim_id in trims:
            url = get_trim_url(model, trim_id)
            urls.append((model, trim_name, url))
    return urls

